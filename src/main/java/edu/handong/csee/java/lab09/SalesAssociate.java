package edu.handong.csee.java.lab09;

public class SalesAssociate {
	String name=null;
	double sales=0;

	void setName(String name) {
		this.name=name;
	}
	void setSales(double sales) {
		this.sales=sales;
	}
	String getName() {
		return this.name;
	}
	double getSales() {
		return this.sales;
	}
}